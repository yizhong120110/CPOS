/*
*动态展示、隐藏内容
*/
function regionSH(title) {
    if(title == 'C端通讯管理信息'){
        $('#divCdtxJbxx').show();
        $('#divJbxx').hide();
    }else if (title == '子流程信息') {
        $('#divCdtxJbxx').hide();
        $('#divJbxx').show();
    } 
}
